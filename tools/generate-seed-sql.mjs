#!/usr/bin/env node
/**
 * Reads curriculum.json and all lesson-plan-expanded.md files,
 * then outputs a SQL script to seed/update the modules table.
 *
 * Usage: node tools/generate-seed-sql.mjs > web/modules-seed.sql
 */

import { readFileSync, existsSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const curriculumDir = join(root, 'curriculum')
const manifestPath = join(curriculumDir, 'curriculum.json')

const manifest = JSON.parse(readFileSync(manifestPath, 'utf-8'))

// Escape single quotes for SQL string literals
function sqlEscape(str) {
  return str.replace(/'/g, "''")
}

const lines = []

lines.push(`-- ============================================================`)
lines.push(`-- Modules seed — generated by tools/generate-seed-sql.mjs`)
lines.push(`-- Run in Supabase SQL Editor to seed all 48 modules with content`)
lines.push(`-- ============================================================`)
lines.push(``)

for (const tier of manifest.tiers) {
  lines.push(`-- ---- ${tier.label} (${tier.age_range}) ----`)

  for (const mod of tier.modules) {
    // Try expanded file first, fall back to original
    const contentFile = mod.content_file
    const expandedPath = join(curriculumDir, contentFile.replace('lesson-plan.md', 'lesson-plan-expanded.md'))
    const originalPath = join(curriculumDir, contentFile)

    let content = ''
    if (existsSync(expandedPath)) {
      content = readFileSync(expandedPath, 'utf-8')
    } else if (existsSync(originalPath)) {
      content = readFileSync(originalPath, 'utf-8')
      console.error(`WARN: Using original (not expanded) for ${tier.slug}/${mod.slug}`)
    } else {
      content = `# ${mod.title}\n\n${mod.description}\n\n*Content coming soon.*`
      console.error(`WARN: No content file found for ${tier.slug}/${mod.slug}`)
    }

    const escaped = sqlEscape(content)
    const desc = sqlEscape(mod.description || '')
    const title = sqlEscape(mod.title)
    const contentPath = sqlEscape(mod.content_file)

    lines.push(`INSERT INTO modules (tier_slug, slug, title, description, order_index, content_path, content, estimated_minutes, enabled)`)
    lines.push(`VALUES (`)
    lines.push(`  '${tier.slug}',`)
    lines.push(`  '${mod.slug}',`)
    lines.push(`  '${title}',`)
    lines.push(`  '${desc}',`)
    lines.push(`  ${mod.order_index},`)
    lines.push(`  '${contentPath}',`)
    lines.push(`  '${escaped}',`)
    lines.push(`  ${mod.estimated_minutes || 30},`)
    lines.push(`  true`)
    lines.push(`)`)
    lines.push(`ON CONFLICT (tier_slug, slug) DO UPDATE SET`)
    lines.push(`  title = EXCLUDED.title,`)
    lines.push(`  description = EXCLUDED.description,`)
    lines.push(`  order_index = EXCLUDED.order_index,`)
    lines.push(`  content_path = EXCLUDED.content_path,`)
    lines.push(`  content = EXCLUDED.content,`)
    lines.push(`  estimated_minutes = EXCLUDED.estimated_minutes,`)
    lines.push(`  enabled = EXCLUDED.enabled;`)
    lines.push(``)
  }
}

lines.push(`-- Done: ${manifest.tiers.reduce((sum, t) => sum + t.modules.length, 0)} modules seeded.`)

process.stdout.write(lines.join('\n'))
